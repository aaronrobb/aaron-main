            <h2 class="title-dash"><span>AODA - Who it involves</span></h2>
        </div>
        <div class="col-md-7 col-12 body-text">
            Ontario has issued a law with the hope of making Ontario as accessible as possible.<br>
            <br>
            This is both in the physical world, and the digital one.<br>
            <br>
            Websites are no exception.<br>
            <br>
            According to the Ontario law, companies and organizations must make new or "significantly refreshed" public-facing websites accessible.<br>
            <br>    
            This means, if you are a private or non-profit organization with 50+ employees, or a public sector organization, your website must be AODA compliant.<br>
            <br>
            As of January 1, 2014, all new websites, significantly altered websites and all new content must be AODA compliant.
        </div>
    </div>
</div>

<div class="container narrow">
    <div class="row mt-3">
        <div class="col-md-5 col-12">
            <h2 class="title-dash"><span>AODA - How to Comply</span></h2>
        </div>
        <div class="col-md-7 col-12 body-text"> 
        If you meet the requirement above, your company's or organization's public-facing website must meet <a href="http://www.w3.org/WAI/intro/wcag.php">WCAG 2.0</a>(Level A) guidelines.<br>
        <br>
        Some examples of how to comply include:<br>
        <ul class="list-group">
            <li class="list-group-item">Guideline 1.1: Provide text alternatives for non-text content</li>
            <li class="list-group-item">Guideline 1.3: Create content that can be presented in different ways (for example simpler layout) without losing information or structure.</li>
            <li class="list-group-item">Guideline 2.1: Make all functionality available from a keyboard.</li>
            <li class="list-group-item">Guideline 3.2: Make Web pages appear and operate in predictable ways.</li>
        </ul>
        <br>
        Essentially, any new website has to meet a certain level of the guidelines to not be in violation.
        </div>
    </div>
</div>

